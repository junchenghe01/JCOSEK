/* 
 * [语法] .syntax 是汇编器指令，unified 是其参数 
 * [语义] 开启“统一汇编语法”模式。
 */
.syntax unified         @ 开启统一汇编语法，支持 ARM 和 Thumb 指令集
/* 
 * [语法] .cpu 是汇编器指令，cortex-a9 是指定的处理器架构参数
 * [语义] 告知汇编器目标硬件为 ARM Cortex-A9 核心。
 */
.cpu cortex-a9          @ 指定目标 CPU 类型，允许汇编器使用该核心特有的指令集（如 ARMv7-A）
/* 
 * [语法] .arch 是汇编器指令，armv7-a 是指定的架构版本参数
 * [语义] 告知汇编器目标处理器遵循 ARMv7-A 架构标准。
 */
.arch armv7-a           @ 指定指令集架构为 ARMv7-A，启用该架构下的所有标准指令（如 A9 核心所属架构）
/* 
 * [语法] .arm 是汇编器伪指令
 * [语义] 强制编译器将随后的代码编译为 32 位的 ARM 指令集。
 */
.arm                    @ 切换到 ARM 状态，指令固定长度为 32 位，追求最高性能

/* 
 * [语法] .section 是段定义指令，.vectors 是段名，"ax" 是段属性参数
 * [语义] 创建或切换到一个名为 .vectors 的代码段，并赋予其“可分配 (a)”和“可执行 (x)”属性。
 */
.section .vectors, "ax"  @ 定义异常向量表专用段，"ax" 确保该段在运行时占用内存并允许 CPU 执行指令

/* -----------------------------------------------------------
 * Os_IntContextSwitch
 * 环境：当前处于 IRQ 模式，且 r0-r12, lr(task) 已压入 IRQ 栈
 * 参数：R0 = old_tcb 指针, R1 = new_tcb 指针 (由汇编逻辑传入)
 * ----------------------------------------------------------- */
/* 
 * [语法] .global 是伪指令，Os_IntContextSwitch 是符号名
 * [语义] 导出中断上下文切换函数，使其能被其他文件（如 IRQ 处理程序）调用。
 */
.global Os_IntContextSwitch
Os_IntContextSwitch:
    /* 1. 获取 IRQ 栈中保存的寄存器并准备清理空间 */
    /* 
     * [语义] 将当前 IRQ 模式的栈指针备份到 r2。此时 IRQ 栈内存放着中断发生时压入的寄存器。
     */
    mov     r2, sp              @ [寄存器操作] 备份 IRQ 栈指针地址
    /* 
     * [语义] 回收 IRQ 栈空间。14个寄存器（R0-R12, LR）共 56 字节。
     */
    add     sp, sp, #56         @ [栈操作] 逻辑上清空 IRQ 栈，防止嵌套中断导致栈溢出

    /* 2. 关键：切换到 SVC 模式来操作任务自己的堆栈 */
    /* 
     * [语法] mrs (Move PSR to Register) 将状态寄存器读入通用寄存器
     * [语义] 读取当前程序状态，保存当前的禁止中断位等信息。
     */
    mrs     r3, cpsr            @ [状态保存] 记录当前 IRQ 模式的状态
    /* 
     * [语法] cps (Change Processor State) 快速切换模式指令
     * [语义] 将 CPU 切换到 SVC 模式（模式号 19/0x13）。任务的堆栈 (SP) 只在 SVC 模式下有效。
     */
    cps     #19                 @ [模式切换] 切换到管理模式 (SVC)

    /* 3. 在 SVC 模式下保存 R4-R11 和 任务返回地址 */
    /* 
     * [语法] ldmia (Load Multiple Increment After) 批量加载
     * [语义] 从 r2 指向的内存（原 IRQ 栈）中恢复 r4-r12 的值到当前寄存器中。
     */
    ldmia   r2, {r4-r12}        @ [现场搬移] 从 IRQ 栈缓冲区取回部分寄存器
    /* 
     * [语义] 从 r2 偏移 52 字节处读取原本保存在 IRQ 栈的 LR（即任务被中断处的 PC）。
     */
    ldr     lr, [r2, #52]       @ [地址提取] 获取任务的断点返回地址

    /* 
     * [语法] push 压栈指令
     * [语义] 将 R4-R11 和返回地址压入“新”任务（其实是旧任务）的 SVC 栈，完成现场封存。
     */
    push    {r4-r11, lr}        @ [现场封存] 构建与任务切换一致的栈帧结构

    /* 4. 保存 SVC 栈指针到 old_tcb */
    /* 
     * [语义] 计算 old_tcb 结构体中 StackPtr 成员的地址（假设偏移为 88）。
     */
    add     r2, r0, #88         @ [地址计算] 获取旧 TCB 的栈指针存储位置
    /* 
     * [语义] 将当前 SVC 模式的 SP 存入旧任务的 TCB。
     */
    str     sp, [r2]            @ [上下文保存] 更新旧任务的栈顶记录

    /* 5. 切换到新任务的堆栈 */
    /* 
     * [语义] 获取新任务 TCB 中存储栈指针的地址。
     */
    add     r3, r1, #88         @ [地址计算] 指向新 TCB 的栈指针成员
    /* 
     * [语义] 从新 TCB 中加载其之前保存的栈顶地址。
     */
    ldr r4, [r3]            @ [上下文恢复] 加载新任务的 SP 值
    /* 
     * [语义] 更新当前系统的 SP，此时 CPU 已经转而使用新任务的堆栈。
     */
    mov     sp, r4              @ [栈切换] 正式切入新任务的环境

    /* 6. 从新任务栈中恢复现场 */
    /* 
     * [语义] 从新任务的栈中弹出之前保存的 R4-R11 和返回地址。
     */
    pop     {r4-r11, lr}        @ [现场恢复] 恢复新任务的执行上下文

    /* 7. 返回并恢复中断状态 */
    /* 
     * [语法] bx lr (Branch and Exchange) 跳转至链接寄存器
     * [语义] 跳转到 lr 指向的地址，程序开始执行新任务的代码逻辑。
     */
    bx      lr                  @ [程序流切换] 完成切换，进入新任务运行


/* ----------------------------------
 * r0 = &old_sp
 * r1 = new_sp
 * ---------------------------------- */
 /* --- 6. 任务切换 Os_ContextSwitch --- */
/* 
 * [语法] .global 是伪指令，Os_ContextSwitch 是符号名
 * [语义] 导出任务级（协同式）上下文切换函数，通常在 C 语言中由调度器触发。
 */
.global Os_ContextSwitch

Os_ContextSwitch:
    /* --- 1. 保存旧任务现场 --- */
    /* 
     * [语义] R0 和 R1 遵循 AAPCS 规则，由调用者传入（r0=旧TCB, r1=新TCB）。
     */

    /* 
     * [语法] PUSH 是批量压栈指令
     * [语义] 将非易失性寄存器 R4-R11 和返回地址 LR 压入当前（旧任务）的栈中。
     * 此时 LR 存储的是 C 语言调用调度器后的下一条指令地址。
     */
    PUSH    {r4-r11, lr}        @ [现场封存] 在旧任务栈中构建基础栈帧

    /* 
     * [语义] 计算 old_tcb 结构体中 StackPtr 成员的绝对地址（假设偏移为 88 字节）。
     */
    add     r2, r0, #88         @ [地址计算] 定位到旧任务 TCB 的栈顶指针保存位置
    /* 
     * [语法] str (Store Register) 存储指令
     * [语义] 将当前 CPU 的堆栈指针 SP 写入旧任务的 TCB 内存中，完成旧现场的持久化。
     */
    str     sp, [r2]            @ [上下文保存] 记录旧任务当前的栈顶地址

    /* --- 2. 恢复新任务现场 --- */
    
    /* 
     * [语义] 计算新任务 TCB 中 StackPtr 成员的地址。
     */
    add     r3, r1, #88         @ [地址计算] 定位到新任务 TCB 的栈顶指针保存位置
    /* 
     * [语法] ldr (Load Register) 加载指令
     * [语义] 从新任务的 TCB 内存中读取之前保存的栈顶地址到寄存器 R4。
     */
    ldr     r4, [r3]            @ [地址提取] 获取新任务的栈顶指针
    /* 
     * [语义] 将 R4 中的值直接赋给硬件 SP 寄存器，从此 CPU 开始使用新任务的内存栈。
     */
    mov     sp, r4              @ [栈切换] 关键一步：切换到新任务的内存环境

    /* 
     * [语法] POP 是批量出栈指令
     * [语义] 从新任务的栈中恢复 R4-R11 和 LR。如果是新任务首次运行，则弹出预设的初始值。
     */
    POP     {r4-r11, lr}        @ [现场恢复] 恢复新任务的寄存器状态

    /* 
     * [语法] bx (Branch and Exchange) 分支跳转指令
     * [语义] 跳转到恢复后的 LR 地址，CPU 返回到新任务上次被切走时的位置继续执行。
     */
    bx      lr                  @ [任务激活] 正式恢复新任务的运行

