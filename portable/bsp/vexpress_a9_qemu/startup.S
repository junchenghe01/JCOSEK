.arm                    @ 切换到 ARM 状态，指令固定长度为 32 位，追求最高性能


/* 
 * [语法] .section 是段定义指令，.vectors 是段名，"ax" 是段属性参数
 * [语义] 创建或切换到一个名为 .vectors 的代码段，并赋予其“可分配 (a)”和“可执行 (x)”属性。
 */
.section .vectors, "ax"  @ 定义异常向量表专用段，"ax" 确保该段在运行时占用内存并允许 CPU 执行指令

/* 
 * [语法] .global 是汇编器伪指令，_start 是指定的符号参数
 * [语义] 将 _start 符号声明为全局可见，使其可以被外部链接器识别。
 */
.global _start          @ 声明程序入口点，告知链接器从此位置开始执行代码


/* 
 * [语法] _start 是一个标签 (Label)，代表当前指令的内存起始地址
 * [语义] 定义程序的主入口点，通常对应异常向量表的第一条指令（复位向量）。
 */
_start:

/* 
 * [语法] ldr (Load Register) 指令，pc 是目标寄存器，=Label 是伪指令语法（加载绝对地址）
 * [语义] 将目标处理函数的绝对地址加载到程序计数器 (PC)，实现强制跳转。
 */
    ldr pc, =Reset_handler      /* [0x00 Reset] 复位向量 系统上电或复位后执行的第一条指令，跳转至初始化程序 */
    ldr pc, =Undef_Handler      /* [0x04 Undefined Instruction] 未定义指令异常 CPU 无法识别指令时跳转至死循环，用于调试捕捉错误 */
    ldr pc, =SVC_Handler        /* [0x08 Software Interrupt (SVC)] 软中断异常 执行 SVC 指令时触发，通常用于用户态请求内核服务 */
    ldr pc, =Prefetch_Handler   /* [0x0C Prefetch Abort] 预取中止异常 指令取址失败（如访问非法地址）时跳转 */
    ldr pc, =Data_Handler       /* [0x10 Data Abort] 数据中止异常 数据读写失败（如内存权限错误）时跳转 */
    
/* 
 * [语法] nop (No Operation) 指令
 * [语义] 空操作，占用 4 字节空间，用于填充 ARM 向量表 0x14 处的保留位。
 */
    nop                         /* [0x14 Reserved] 保留位 保持向量表偏移量正确，遵循 ARM 架构规范 */

    ldr pc, =IRQ_Handler        /* [0x18 IRQ] 外部中断请求 外设触发中断时跳转，处理如定时器、串口等异步事件 */
    ldr pc, =FIQ_Handler       /* [0x1C FIQ] 快速中断请求 高优先级中断入口，本示例中未处理则跳转至死循环 */
    /*TODO: 中断向量表 (Vector Table)：
硬件跳转的入口。Category 2 的向量通常指向一个统一汇编入口，而不是直接指向用户的 C 函数。 */
    @ ldr pc, =IRQ_2_Handler 二类中断基于一类中断，咋
/* 
 * [语法] .section 是段定义指令，.text 是预定义的段名参数
 * [语义] 切换到代码段（Text Section），告知汇编器随后的指令应存放在受保护的只读存储区域。
 */
.section .text          @ 指示接下来的内容为程序指令，通常存放在 Flash 或内存的只读区域
/* 
 * [语法] Reset_handler 是复位异常入口标签
 * [语义] 系统上电或复位后的执行起点，负责最底层的硬件环境初始化。
 */
Reset_handler:
    /* 
     * [语法] cpsid (Change Processor State, Interrupt Disable) 指令，参数 i,f 代表屏蔽 IRQ 和 FIQ
     * [语义] 硬件初始化阶段必须全局关中断，防止未准备就绪的中断服务程序导致系统崩溃。
     */
    cpsid if

    /* --- 2. 设置向量表基地址 (VBAR) --- */
    /* 
     * [语法] mcr (Move to Coprocessor from ARM Register) 指令，操作 p15 协处理器的 c12 寄存器
     * [语义] 将 _start 标签所在的内存物理地址写入 VBAR 寄存器，重定向 CPU 的异常跳转入口。
     */
    ldr r0, =_start
    mcr p15, 0, r0, c12, c0, 0

    /* --- 3. 初始化各模式堆栈 --- */
    
    /* 
     * [语法] msr (Move to Special Register) 指令，修改当前程序状态寄存器 (CPSR) 的控制域
     * [语义] 切换至 IRQ 模式，以便在该模式特有的寄存器组中初始化栈指针 (sp)。
     */
    msr cpsr_c, #0xD2
    ldr sp, =__irq_stack_top    /* 为中断处理预留独立的内存栈空间 */

    /* 
     * [语义] 切换回 SVC (Supervisor) 模式，这是操作系统内核运行的默认特权等级。
     */
    msr cpsr_c, #0xD3
    ldr sp, =__stack_top        /* 设置主内核栈，支撑 C 语言函数的局部变量和调用链 */

    /* --- 4. 清 BSS --- */
    /* 
     * [语义] 准备 BSS 段（存放未初始化全局变量）的起始地址 (r0) 和结束地址 (r1)，并将清零值 (r2=0) 准备好。
     */
    ldr r0, =__bss_start
    ldr r1, =__bss_end
    mov r2, #0

/* 
 * [语义] 循环结构：逐个地址将 BSS 区域清零，确保符合 C 语言标准。
 */
bss_loop:
    /* [语法] cmp (Compare) 指令，比较当前指针与结束边界 */
    cmp r0, r1
    /* [语法] strlo (Store Lower) 条件指令，[r0], #4 表示后增量寻址 */
    /* [语义] 如果 r0 < r1，则将 0 写入 [r0] 指向的内存，并将 r0 指针向后移动 4 字节 */
    strlo r2, [r0], #4
    /* [语法] blo (Branch if Lower) 跳转指令 */
    /* [语义] 若未到达边界，循环执行清零操作 */
    blo bss_loop

    /* 
     * [语法] bl (Branch with Link) 跳转并链接指令
     * [语义] 跳转到 C 语言入口 main 函数，并将返回地址存入 lr，标志着从汇编环境进入高级语言环境。
     */
    bl main

/* 
 * [语义] 如果 main 函数意外退出，程序将进入此死循环，防止 CPU 跑飞。
 */
dead_loop:
    b .

