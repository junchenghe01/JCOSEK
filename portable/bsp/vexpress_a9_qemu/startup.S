@ 1. 通用寄存器 r0-r12
@ 这些寄存器主要用于数据操作和临时存储。

@ r0-r3
@ 用途：用于传递参数给函数，以及从函数返回结果。
@ 说明：在函数调用中，前4个参数通过r0-r3传递。如果参数超过4个，则使用栈传递。同时，r0也用于存放函数的返回值。
@ 函数调用参数传递：
@ 函数参数1 → r0
@ 参数2     → r1  
@ 参数3     → r2
@ 参数4     → r3
@ 返回值    → r0（函数返回时）
@ r4-r11
@ 用途：用于保存局部变量。
@ 说明：在函数调用中，这些寄存器的值必须被保存（callee-saved）。也就是说，如果函数要使用这些寄存器，必须先将它们原来的值压栈保存，然后在返回前恢复。

@ r12 (IP)
@ 用途：内部过程调用暂存寄存器（Intra-Procedure-call scratch register）。
@ 说明：在函数调用时，它可能被用于保存一些中间值，但通常不被视为必须保存的寄存器。

@ 2. 特殊寄存器 r13-r15
@ r13 (SP) - 栈指针寄存器
@ 用途：指向当前栈顶。
@ 说明：ARM使用满递减栈（Full Descending Stack），即栈向低地址方向增长，栈指针指向最后压入栈的数据。在上下文切换时，必须保存和恢复每个任务的栈指针。

@ r14 (LR) - 链接寄存器
@ 用途：用于保存子程序的返回地址。
@ 说明：当执行分支跳转指令（如BL）时，处理器自动将返回地址（下一条指令的地址）保存到LR中。在函数返回时，通常将LR的值复制到PC中即可返回。

@ r15 (PC) - 程序计数器
@ 用途：指向当前正在执行的指令的地址。
@ 说明：在ARM状态下，PC的值为当前指令地址加8（由于流水线）。修改PC的值将导致程序跳转到新的地址。

@ 3. 程序状态寄存器 CPSR
@ CPSR（Current Program Status Register）用于存储当前处理器的状态。
@ 条件标志位
@ N（Negative）：如果结果为负，则置1。
@ Z（Zero）：如果结果为零，则置1。
@ C（Carry）：如果结果有进位或借位，则置1。
@ V（Overflow）：如果结果有溢出，则置1。

@ 模式位
@ M[4:0]：处理器模式位。例如，用户模式、系统模式、各种异常模式（如IRQ、FIQ、SVC等）。
@ 中断禁止位
@ I：置1时禁止IRQ中断。
@ F：置1时禁止FIQ中断。

@ 其他位
@ T：置0表示ARM状态，置1表示Thumb状态。
@ Q：用于DSP指令的溢出标志。

@ 4. 其他寄存器
@ 在异常模式下，有对应的SPSR（Saved Program Status Register）
@ 当发生异常时，CPSR的值保存到对应异常模式的SPSR中，然后CPSR设置为异常模式。从异常返回时，通过SPSR恢复CPSR。
@ 在异常模式下，还有一组独立的寄存器
@ 例如，在FIQ模式下，有r8_fiq到r14_fiq，这样在进入FIQ中断时，可以不用保存这些寄存器，从而加速中断处理。

@ 5. 在上下文切换中的寄存器处理
@ 在上下文切换中，我们需要保存和恢复任务的上下文。这包括：
@ 通用寄存器：r0-r12，但通常我们只保存被调用者保存的寄存器（r4-r11），因为调用者保存的寄存器（r0-r3，r12）由调用者负责。
@ 栈指针：sp（r13）
@ 链接寄存器：lr（r14）
@ 程序计数器：pc（r15），但实际上我们通过保存lr来间接保存pc（因为任务切换时，任务被切换出去时的返回地址就是任务恢复后应该执行的地址）
@ 程序状态寄存器：cpsr，但在任务切换时，我们通常不直接保存cpsr，因为任务的运行状态（如条件标志）对于任务来说并不关键，而且任务切换通常是在特权模式下进行的，cpsr的模式位会被正确设置。但是，如果任务运行在用户模式，那么cpsr中的条件标志位是任务状态的一部分，需要保存。然而，在简单的上下文切换中，我们通常不保存cpsr，因为任务切换是通过软件协作的，任务在调用切换函数时已经保存了条件寄存器（如果有需要）。
@ 在之前的上下文切换代码中，我们只保存了r4-r11和lr，这是因为：
@ r0-r3和r12是调用者保存寄存器，在调用context_switch函数时，由调用者负责保存（如果需要）。
@ sp的保存是通过str sp, [r0]将当前的栈指针保存到当前任务的控制块中。
@ pc不需要显式保存，因为任务被切换出去时，其返回地址（即任务恢复后应该执行的地址）已经保存在lr中，而lr被保存到了栈中。

@ 6. 完整的上下文切换需要保存的寄存器
@ 在实际的操作系统中，上下文切换可能需要保存更多的寄存器，包括：
@ 所有的通用寄存器（r0-r15）
@ cpsr
@ 浮点寄存器（如果有的话）
@ 其他协处理器寄存器
@ 但为了效率，我们通常只保存必要寄存器。在ARM中，通过函数调用进行任务切换，利用函数调用的寄存器保存约定，可以简化上下文切换。

