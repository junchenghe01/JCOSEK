IRQ_Handler:
    /* 1. 硬件现场保存 */
    sub     lr, lr, #4
    stmfd   sp!, {r0-r12, lr}   @ 压入 IRQ 模式栈

    /* 2. 进入 C 环境处理核心逻辑 */
    /* 所有的嵌套计数、一类/二类区分、Timer处理、调度判定都在此函数中 */
    bl      Os_Irq_Common_Handler

    /* 3. 检查调度请求 */
    /* 我们约定：如果 Os_Irq_Common_Handler 认为需要调度，它会返回 1 (r0) */
    cmp     r0, #1
    bne     irq_exit            @ 结果不为1，直接返回

    /* --- 执行中断级抢占切换 --- */
    /* 此时仍处于 IRQ 模式，需调用上下文切换将 IRQ 栈中的数据转移到 TCB */
    bl      Os_IntContextSwitch  

irq_exit:
    /* 4. 恢复现场并返回 */
    ldmfd   sp!, {r0-r12, pc}^ 

